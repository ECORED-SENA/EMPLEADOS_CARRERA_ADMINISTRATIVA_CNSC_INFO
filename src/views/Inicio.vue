      span.h4 2. Justificación<br>del programa

    p La formación complementaria del Curso para empleados de carrera administrativa sujetos al sistema tipo de evaluación de la CNSC que se desarrolla a través del Servicio Nacional de Aprendizaje - SENA tiene como objetivo reconocer desde el rol de evaluado, la evaluación del desempeño como herramienta de gestión, en la que los empleados públicos de carrera administrativa y en período de prueba deben ajustar sus acciones y conductas, mediante la articulación de los compromisos funcionales y comportamentales con las metas de la dependencia, para responder a las necesidades y expectativas de la ciudadanía y al logro del cumplimiento de objetivos de la entidad.

    p.mt-3 A partir del 1° de febrero de 2019 se deroga el Acuerdo 565 de 2016 de la Comisión Nacional del Servicio Civil (CNSC) y entra en vigor el Acuerdo 617 de 2018, expedido por la misma Comisión, el cual establece el Sistema tipo de evaluación de desempeño laboral para los servidores de carrera administrativa y en período de prueba mediante la aplicación de diferentes fases y con la participación de diversos actores.

    p.mt-3 El nuevo acuerdo contempla cambios con respecto a lo definido en el Acuerdo 565: cuatro fases en lugar de cinco para llevar a cabo el proceso de evaluación, indica que el responsable de la conformación de la Comisión evaluadora es el Jefe de la Unidad de Personal o quien haga sus veces, cambian también los períodos de concertación de compromisos, períodos de seguimiento, los porcentajes de los compromisos laborales, en los cuales se debe tener en cuenta que “la evaluación de gestión por áreas o dependencias no se tendrá en cuenta en el marco del proceso de calificación; sin embargo, se considera un insumo para la concertación de compromisos”.

    p.mt-3 En la escala de los compromisos comportamentales se ajustaron las descripciones y los resultados numéricos por nivel de desarrollo; en cuanto a niveles de cumplimiento, el nivel destacado fue suspendido por el Consejo de Estado. Como instrumento de evaluación se cuenta con un aplicativo diseñado por la CNSC, reemplazando los once formatos en Excel que se tenían definidos para llevar a cabo el proceso de evaluación del desempeño.

